package com.peclab.nurgissa.thunderlist;


public class Task {
    private String title;

    public Task() {}

    public Task(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return title;
    }
}
